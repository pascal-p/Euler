using Test

include("./max-total.jl")

@testset "4 rows" begin
  input = """
  3
  7 4
  2 4 6
  8 5 9 3
  """

  @test calc_max_path(input; show_path=true) == (23, [(1, 1), (2, 1), (3, 2), (4, 3)])
end

